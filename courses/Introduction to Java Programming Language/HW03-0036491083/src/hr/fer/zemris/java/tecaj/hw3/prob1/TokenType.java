package hr.fer.zemris.java.tecaj.hw3.prob1;

/**
 * This enum contains 4 constants of type TokenType. Those constants are used
 * for describing what kind of a token some particular token is. This behaviour
 * is implemented in {@link Token} class.
 * 
 * @author Leonardo Kokot
 * @version 1.0
 *
 */
public enum TokenType {

	/**
	 * One of possible values of this enumeration.
	 */
	EOF,

	/**
	 * One of possible values of this enumeration.
	 */
	WORD,

	/**
	 * One of possible values of this enumeration.
	 */
	NUMBER,

	/**
	 * One of possible values of this enumeration.
	 */
	SYMBOL;

}
